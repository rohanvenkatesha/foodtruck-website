// --- Layout & Placement Data (The heart of the collage) ---
function generatePlacements(count: number) {
  const base = [
    { gridColumn: '1 / span 5', gridRow: '1 / span 4', zIndex: 1, rotation: -4 },
    { gridColumn: '5 / span 4', gridRow: '2 / span 5', zIndex: 2, rotation: 2 },
    { gridColumn: '9 / span 4', gridRow: '1 / span 3', zIndex: 1, rotation: 5 },
    { gridColumn: '2 / span 4', gridRow: '5 / span 4', zIndex: 3, rotation: 3 },
    { gridColumn: '8 / span 5', gridRow: '4 / span 4', zIndex: 4, rotation: -3 },
    { gridColumn: '1 / span 3', gridRow: '3 / span 3', zIndex: 5, rotation: 6 },
    { gridColumn: '6 / span 3', gridRow: '1 / span 2', zIndex: 6, rotation: -5 },
    { gridColumn: '7 / span 5', gridRow: '7 / span 2', zIndex: 1, rotation: 4 },
  ];

  const placements = [];
  for (let i = 0; i < count; i++) {
    placements.push({ ...base[i % base.length] });
  }
  return placements;
}

